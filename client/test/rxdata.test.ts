import { describe, it, expect } from 'vitest';
import { parseRxdata } from '../src/lib/rxdata';

describe('parseRxdata', () => {
  it('parses simple rxdata', () => {
    const buf = Uint8Array.from([
      0x04, 0x08, 0x5b, 0x01, 0x01, 0x7b, 0x01, 0x02, 0x22, 0x01, 0x07, 0x73, 0x70, 0x65, 0x63, 0x69, 0x65, 0x73, 0x22, 0x01, 0x07, 0x70, 0x69, 0x6b, 0x61, 0x63, 0x68, 0x75, 0x22, 0x01, 0x04, 0x6e, 0x61, 0x6d, 0x65, 0x22, 0x01, 0x06, 0x53, 0x70, 0x61, 0x72, 0x6b, 0x79,
    ]);
    const { team } = parseRxdata(buf.buffer);
    expect(team[0]).toMatchObject({ species: 'pikachu', nick: 'Sparky' });
  });
});
